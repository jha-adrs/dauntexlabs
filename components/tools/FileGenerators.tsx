/* ── file type config ────────────────────────────────────────────────────── */

type FileType = 'csv' | 'json' | 'xml' | 'html' | 'markdown' | 'text' | 'png'

interface FileTypeConfig {
  label: string
  ext: string
  mime: string
  hasRows: boolean
  isText: boolean
}

const FILE_CONFIGS: Record<FileType, FileTypeConfig> = {
  csv:      { label: 'CSV',        ext: 'csv',  mime: 'text/csv',         hasRows: true,  isText: true  },
  json:     { label: 'JSON',       ext: 'json', mime: 'application/json', hasRows: true,  isText: true  },
  xml:      { label: 'XML',        ext: 'xml',  mime: 'application/xml',  hasRows: true,  isText: true  },
  html:     { label: 'HTML',       ext: 'html', mime: 'text/html',        hasRows: false, isText: true  },
  markdown: { label: 'Markdown',   ext: 'md',   mime: 'text/markdown',    hasRows: false, isText: true  },
  text:     { label: 'Plain Text', ext: 'txt',  mime: 'text/plain',       hasRows: false, isText: true  },
  png:      { label: 'PNG Image',  ext: 'png',  mime: 'image/png',        hasRows: false, isText: false },
}

const FILE_TYPE_OPTIONS = (Object.entries(FILE_CONFIGS) as [FileType, FileTypeConfig][]).map(
  ([value, { label }]) => ({ value, label })
)

/* ── PNG canvas generation ───────────────────────────────────────────────── */

const PNG_W = 600
const PNG_H = 400

function drawCanvas(canvas: HTMLCanvasElement) {
  const ctx = canvas.getContext('2d')
  if (!ctx) return

  canvas.width  = PNG_W
  canvas.height = PNG_H

  // Background
  ctx.fillStyle = '#0a0b09'
  ctx.fillRect(0, 0, PNG_W, PNG_H)

  // Acid border (4px inset)
  ctx.strokeStyle = '#c6f24e'
  ctx.lineWidth = 4
  ctx.strokeRect(2, 2, PNG_W - 4, PNG_H - 4)

  // Inner decorative frame
  ctx.strokeStyle = 'rgba(198,242,78,0.25)'
  ctx.lineWidth = 1
  ctx.strokeRect(20, 20, PNG_W - 40, PNG_H - 40)

  // Corner ticks
  const tick = 16
  ctx.strokeStyle = '#c6f24e'
  ctx.lineWidth = 2
  const corners: [number, number, number, number][] = [
    [20, 20, 20 + tick, 20], [20, 20, 20, 20 + tick],
    [PNG_W - 20, 20, PNG_W - 20 - tick, 20], [PNG_W - 20, 20, PNG_W - 20, 20 + tick],
    [20, PNG_H - 20, 20 + tick, PNG_H - 20], [20, PNG_H - 20, 20, PNG_H - 20 - tick],
    [PNG_W - 20, PNG_H - 20, PNG_W - 20 - tick, PNG_H - 20], [PNG_W - 20, PNG_H - 20, PNG_W - 20, PNG_H - 20 - tick],
  ]
  corners.forEach(([x1, y1, x2, y2]) => { ctx.beginPath(); ctx.moveTo(x1, y1); ctx.lineTo(x2, y2); ctx.stroke() })

  // Dimensions label
  ctx.fillStyle = '#c6f24e'
  ctx.font = 'bold 32px monospace'
  ctx.textAlign = 'center'
  ctx.textBaseline = 'middle'
  ctx.fillText(`${PNG_W} × ${PNG_H}`, PNG_W / 2, PNG_H / 2 - 22)

  // Subtitle
  ctx.fillStyle = 'rgba(198,242,78,0.6)'
  ctx.font = '14px monospace'
  ctx.fillText('dauntexlabs · sample image', PNG_W / 2, PNG_H / 2 + 18)

  // Timestamp
  ctx.fillStyle = 'rgba(198,242,78,0.35)'
  ctx.font = '11px monospace'
  ctx.fillText(new Date().toISOString(), PNG_W / 2, PNG_H / 2 + 46)
}

/* ── component ──────────────────────────────────────────────────────────── */

export default function FileGenerators() {
  const [fileType, setFileType] = useState<FileType>('csv')
  const [rows, setRows] = useState('10')
  const [content, setContent] = useState('')
  const [pngUrl, setPngUrl] = useState<string | null>(null)

  const canvasRef = useRef<HTMLCanvasElement>(null)
  const prevPngUrl = useRef<string | null>(null)

  const cfg = FILE_CONFIGS[fileType]

  const generateText = useCallback((type: FileType, rowCount: number): string => {
    switch (type) {
      case 'csv':      return generateCSV(rowCount)
      case 'json':     return generateJSON(rowCount)
      case 'xml':      return generateXML(rowCount)
      case 'html':     return generateHTML()
      case 'markdown': return generateMarkdown()
      case 'text':     return generatePlainText()
      default:         return ''
    }
  }, [])

  function handleGenerate() {
    if (fileType === 'png') {
      const canvas = canvasRef.current
      if (!canvas) return
      drawCanvas(canvas)
      // Revoke old object URL
      if (prevPngUrl.current) URL.revokeObjectURL(prevPngUrl.current)
      canvas.toBlob((blob) => {
        if (!blob) return
        const url = URL.createObjectURL(blob)
        prevPngUrl.current = url
        setPngUrl(url)
      }, 'image/png')
      setContent('')
    } else {
      const n = Math.max(1, Math.min(500, parseInt(rows) || 10))
      const text = generateText(fileType, n)
      setContent(text)
      setPngUrl(null)
    }
  }

  // Regenerate when type changes to keep preview fresh
  useEffect(() => {
    setContent('')
    setPngUrl(null)
  }, [fileType])

  function downloadPng() {
    if (!pngUrl) return
    const a = document.createElement('a')
    a.href = pngUrl
    a.download = 'sample-image.png'
    document.body.appendChild(a)
    a.click()
    document.body.removeChild(a)
  }

  return (
    <>
      <Toolbar>
        <Field label="File type">
          <Select
            value={fileType}
            onChange={(v) => setFileType(v as FileType)}
            options={FILE_TYPE_OPTIONS}
          />
        </Field>
        {cfg.hasRows && (
          <Field label="Rows">
            <TextInput value={rows} onChange={setRows} type="number" placeholder="10" />
          </Field>
        )}
        <Button variant="primary" onClick={handleGenerate}>
          Generate
        </Button>
      </Toolbar>

      {/* Hidden canvas for PNG generation */}
      <canvas ref={canvasRef} style={{ display: 'none' }} />

      {fileType === 'png' ? (
        <IO>
          <Panel
            title="PNG preview"
            actions={
              pngUrl ? (
                <Button variant="ghost" onClick={downloadPng}>
                  download PNG
                </Button>
              ) : undefined
            }
          >
            {pngUrl ? (
              <img
                src={pngUrl}
                alt={`Generated ${PNG_W}×${PNG_H} PNG`}
                style={{
                  maxWidth: '100%',
                  borderRadius: 4,
                  border: '1px solid var(--line)',
                  display: 'block',
                }}
              />
            ) : (
              <span style={{ color: 'var(--mute)', fontSize: '0.85rem' }}>
                Click Generate to produce a PNG image.
              </span>
            )}
          </Panel>
        </IO>
      ) : (
        <IO>
          <Panel
            title={`${cfg.label.toLowerCase()} preview`}
            actions={
              content ? (
                <>
                  <CopyButton text={content} />
                  <DownloadButton
                    text={content}
                    filename={`sample.${cfg.ext}`}
                    mime={cfg.mime}
                    label={`download .${cfg.ext}`}
                  />
                </>
              ) : undefined
            }
          >
            <TextArea
              value={content}
              readOnly
              placeholder={`Click Generate to produce a sample ${cfg.label} file…`}
              rows={18}
            />
          </Panel>
        </IO>
      )}
    </>
  )
}
