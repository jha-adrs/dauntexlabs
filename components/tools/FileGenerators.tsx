/* ── random helpers ──────────────────────────────────────────────────────── */

function randInt(min: number, max: number): number {
  const arr = new Uint32Array(1)
  crypto.getRandomValues(arr)
  return min + (arr[0] % (max - min + 1))
}

function randFloat(min: number, max: number): number {
  const arr = new Uint32Array(1)
  crypto.getRandomValues(arr)
  return min + (arr[0] / 0xffffffff) * (max - min)
}

function pickFrom<T>(arr: T[]): T {
  return arr[randInt(0, arr.length - 1)]
}

function uuidv4(): string {
  if (typeof crypto.randomUUID === 'function') return crypto.randomUUID()
  const b = new Uint8Array(16)
  crypto.getRandomValues(b)
  b[6] = (b[6] & 0x0f) | 0x40
  b[8] = (b[8] & 0x3f) | 0x80
  const h = Array.from(b).map((x) => x.toString(16).padStart(2, '0'))
  return [h.slice(0,4).join(''), h.slice(4,6).join(''), h.slice(6,8).join(''), h.slice(8,10).join(''), h.slice(10).join('')].join('-')
}

const FIRST_NAMES = ['Alice','Bob','Carol','David','Elena','Frank','Grace','Hiro','Iris','James','Kira','Liam','Maya','Noah','Olivia','Paulo','Quinn','Rosa','Sam','Tara']
const LAST_NAMES  = ['Adams','Baker','Chen','Diaz','Evans','Foster','Garcia','Hayes','Iyer','Jones','Kim','Lopez','Miller','Nguyen','Ortiz','Patel','Quinn','Ramos','Smith','Torres']
const DOMAINS     = ['example.com','testmail.io','fakemail.net','devtest.org','sample.co']

function randRecord(index: number): { id: string; name: string; email: string; date: string; amount: string } {
  const first = pickFrom(FIRST_NAMES)
  const last  = pickFrom(LAST_NAMES)
  const d     = new Date(randInt(1577836800000, 1735689600000)) // 2020-2025
  return {
    id:     String(index + 1),
    name:   `${first} ${last}`,
    email:  `${first.toLowerCase()}.${last.toLowerCase()}${randInt(1,99)}@${pickFrom(DOMAINS)}`,
    date:   d.toISOString().slice(0, 10),
    amount: randFloat(10, 9999).toFixed(2),
  }
}

function makeRecords(rows: number) {
  const n = Math.max(1, Math.min(500, rows))
  return Array.from({ length: n }, (_, i) => randRecord(i))
}

/* ── generators ─────────────────────────────────────────────────────────── */

function generateCSV(rows: number): string {
  const header = 'id,name,email,date,amount'
  const lines = makeRecords(rows).map(
    r => `${r.id},"${r.name}",${r.email},${r.date},${r.amount}`
  )
  return [header, ...lines].join('\n')
}

function generateJSON(rows: number): string {
  return JSON.stringify(makeRecords(rows), null, 2)
}

function generateXML(rows: number): string {
  const records = makeRecords(rows)
  const items = records
    .map(r =>
      `  <record>\n    <id>${r.id}</id>\n    <name>${r.name}</name>\n    <email>${r.email}</email>\n    <date>${r.date}</date>\n    <amount>${r.amount}</amount>\n  </record>`
    )
    .join('\n')
  return `<?xml version="1.0" encoding="UTF-8"?>\n<records>\n${items}\n</records>`
}

